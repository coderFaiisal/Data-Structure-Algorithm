const numbers = [23, 56, 34, 2, 56, 90, 3];

function bubbleSortAscending(array) {
  for (let i = 0; i < array.length; i++) {
    for (let j = 0; j < array.length; j++) {
      if (array[j] > array[j + 1]) {
        const temp = array[j];
        array[j] = array[j + 1];
        array[j + 1] = temp;
      }
    }
  }
  return array;
}

const result = bubbleSortAscending(numbers);
console.log(result);

